Info operator+(const Info &a, const Info &b) {
    return Info{max(a.x, b.x)};
}

void solve() {
    int n, q;
    cin >> n >> q;
    vector<vector<int>> g(n);
    for (int i = 0; i < n - 1; i++) {
        int x, y;
        cin >> x >> y;
        x--, y--;
        g[x].push_back(y);
        g[y].push_back(x);
    }
    vector<int> p(n);
    for (int i = 0; i < n; i++) {
        cin >> p[i];
        p[i]--;
    }

    int t = 0;
    vector<int> in(n), out(n);
    auto dfs = [&](this auto &&self, int x, int fa) -> void {
        in[x] = t;
        t++;
        for (auto &y : g[x]) {
            if (y != fa) {
                self(y, x);
            }
        }
        out[x] = t;
    };
    dfs(0, -1);

    vector<vector<tuple<int, int, int>>> qs(n);
    for (int i = 0; i < q; i++) {
        int l, r, x;
        cin >> l >> r >> x;
        l--, r--, x--;
        // [0, l - 1] 在in(x), out(x)区间内的贡献要删去
        if (l > 0) {
            qs[l - 1].emplace_back(x, -1, i);
        }
        qs[r].emplace_back(x, 1, i);
    }
    vector<int> ans(q);
    Fenwick<int> fen(n);
    for (int i = 0; i < n; i++) {
        // 添加首次访问的时间戳
        fen.add(in[p[i]], 1);
        for (auto &[x, d, idx] : qs[i]) {
            ans[idx] += d * fen.rangeSum(in[x], out[x]);
        }
    }
    for (auto &cnt_in_range : ans) {
        cout << (cnt_in_range > 0 ? "YES" : "NO") << endl;
    }
    cout << endl;
}

int main() {
    cin.tie(nullptr)->sync_with_stdio(false);
    cout << fixed << setprecision(20);
    int T = 1;
#if MULTICASE
    cin >> T;
#endif
    while (T--) {
        solve();
    }
    return 0;
}