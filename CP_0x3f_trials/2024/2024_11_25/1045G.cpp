void solve() {
    int n, k;
    cin >> n >> k;
    vector<vector<int>> robots;
    unordered_map<int, vector<int>> g;
    for (int i = 0; i < n; i++) {
        int x, r, q;
        cin >> x >> r >> q;
        robots.push_back({x, r, q});
        g[q].push_back(x);
    }

    // 根据半径降序排列
    sort(robots.begin(), robots.end(), [&](auto &e1, auto &e2) {
        return e1[1] > e2[1];
    });

    unordered_map<int, Fenwick<int>> fens;
    for (auto &[q, xs] : g) {
        sort(xs.begin(), xs.end());
        fens[q] = Fenwick<int>(xs.size());
    }

    i64 ans = 0;
    for (auto &robot : robots) {
        int x = robot[0], r = robot[1], q = robot[2];
        // k范围小，枚举iq
        for (int iq = q - k; iq <= q + k; iq++) {
            // 位置[x - r, x + r]
            if (g.find(iq) == g.end()) continue;
            int R = upper_bound(g[iq].begin(), g[iq].end(), x + r) - g[iq].begin();
            int L = lower_bound(g[iq].begin(), g[iq].end(), x - r) - g[iq].begin();
            ans += fens[iq].rangeSum(L, R);
        }
        int pos = lower_bound(g[q].begin(), g[q].end(), x) - g[q].begin();
        fens[q].add(pos, 1);
    }
    cout << ans << endl;
}

