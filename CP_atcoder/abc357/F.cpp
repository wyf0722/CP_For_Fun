struct Tag {
    Z addx;
    Z addy;
 
    Tag(Z _addx = 0, Z _addy = 0) : addx(_addx), addy(_addy) {}
 
    void apply(const Tag &t) {
        addx += t.addx;
        addy += t.addy;
    }
};
 
struct Info {
    Z sx;
    Z sy;
    Z sxy;
    Z sz;
 
    Info(Z _sx = 0, Z _sy = 0, Z _sxy = 0, Z _sz = 0) : sx(_sx), sy(_sy), sxy(_sxy), sz(_sz) {}
 
    void apply(const Tag &t) {
        sxy += sx * t.addy + sy * t.addx + sz * t.addx * t.addy;
        sx += t.addx * sz;
        sy += t.addy * sz;
    }
};
 
// use Info or const Info &
Info operator+(const Info &a, const Info &b) {
    return Info{a.sx + b.sx, a.sy + b.sy, a.sxy + b.sxy, a.sz + b.sz};
}

