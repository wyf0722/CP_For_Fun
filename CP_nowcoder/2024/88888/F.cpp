void solve() {
    int n;
    cin >> n;
    vector<vector<int>> v(n);
    vector<multiset<int>> s(n);
    SegmentTree<Info> seg(n);
    for (int i = 0; i < n; i++) {
        int m;
        cin >> m;
        v[i].resize(m);
        for (int j = 0; j < m; j++) {
            cin >> v[i][j];
            s[i].insert(v[i][j]);
        }
        seg.modify(i, *s[i].begin());
    }
    int q;
    cin >> q;
    while (q--) {
        int op;
        cin >> op;
        if (op == 1) {
            int i, j, x;
            cin >> i >> j >> x;
            i--, j--;
            s[i].erase(s[i].find(v[i][j]));
            v[i][j] = x;
            s[i].insert(x);
            seg.modify(i, *s[i].begin());
        } else {
            int i;
            cin >> i;
            cout << seg.rangeQuery(0, i).x << endl;
        }
    }
}

