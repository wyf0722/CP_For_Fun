void solve() {
    int n, q;
    cin >> n >> q;
    vector<int> a(n);
    for (int i = 0; i < n; i++) {
        cin >> a[i];
    }
    vector<int> d(n);
    for (int i = 0; i < n - 1; i++) {
        d[i] = abs(a[i] - a[i + 1]);
    }
    SparseTable st(d, [&](int i, int j) {
        return gcd(i, j);
    });
    for (int i = 0; i < q; i++) {
        int l, r;
        cin >> l >> r;
        l--, r--;
        cout << (l == r ? 0 : st.query(l, r - 1)) << " \n"[i == q - 1];
    }
}

