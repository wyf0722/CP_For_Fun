struct Tag {
    i64 x = 0;
 
    void apply(const Tag &t) {
        x += t.x;
    }
};

struct Info {
    i64 mx = 0;
    i64 mx2 = 0;
    int range = 0; // 区间长度
 
    void apply(const Tag &t) {
        mx += t.x;
        if (range >= 2) mx2 += t.x;
    }
};

Info operator+(const Info &a, const Info &b) {
    i64 mx = max(a.mx, b.mx);
    i64 mx2 = max(min(a.mx, b.mx), max(a.mx2, b.mx2));
    return Info{mx, mx2, a.range + b.range};
}

void solve() {
    int n, q;
    cin >> n >> q;
    vector<i64> a(n);
    LazySegmentTree<Info, Tag> seg(n);
    i64 tot = 0LL;
    auto get_max = [&]() -> i64 { return seg.rangeQuery(0, n).mx; };
    auto get_max2 = [&]() -> i64 { return seg.rangeQuery(0, n).mx2; };
    auto work = [&]() -> i64 {
        // 如果最大值小于2，或者只有一种卡牌，无法保证
        if (get_max() == tot || get_max() < 2) {
            return -1;
        }
        // 除了最大值其余全是1的形式
        if (get_max2() <= 1) return get_max() - 1;
        return get_max();
    };

    for (int i = 0; i < n; i++) {
        cin >> a[i];
        seg.modify(i, Info{a[i], 0, 1});
        tot += a[i];
    }
    cout << work() << endl;
    while (q--) {
        char op;
        int l, r, x;
        cin >> op >> l >> r >> x;
        l--, r--;
        tot += (op == '+' ? 1LL : -1LL) * x * (r - l + 1);
        seg.rangeApply(l, r + 1, (op == '+' ? Tag{x} : Tag{-x}));
        cout << work() << endl;
    }
}

