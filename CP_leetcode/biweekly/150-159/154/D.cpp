class Solution {
public:
    vector<int> treeQueries(int n, vector<vector<int>>& edges, vector<vector<int>>& queries) {
        HLD hld(n);
        vector<int> val(n);
        for (auto &e : edges) {
            int u = e[0] - 1, v = e[1] - 1, w = e[2];
            hld.addEdge(u, v);
        }
        hld.work();
        SegmentTree<int> seg(n);

        auto update = [&](int u, int v, int w) -> void {
            if (hld.dep[u] > hld.dep[v]) swap(u, v);
            seg.modify(hld.in[v], w);
        };

        for (auto &e : edges) {
            int u = e[0] - 1, v = e[1] - 1, w = e[2];
            update(u, v, w);
        }

        vector<int> ans;
        for (auto &q : queries) {
            if (q[0] == 1) {
                int u = q[1] - 1, v = q[2] - 1, w = q[3];
                update(u, v, w);
            } else {
                int node = q[1] - 1;
                int res = 0;
                while (node >= 0) {
                    int top = hld.top[node];
                    res += seg.rangeQuery(hld.in[top], hld.in[node] + 1);
                    node = hld.parent[top];
                }
                ans.push_back(res);
            }
        }

        return ans;
    }
};