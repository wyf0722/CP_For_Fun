struct DSU {
    std::vector<int> f, siz;

    DSU() {}
    DSU(int n) {
        init(n);
    }

    void init(int n) {
        f.resize(n);
        std::iota(f.begin(), f.end(), 0);
        siz.assign(n, 1);
    }

    int find(int x) {
        while (x != f[x]) {
            x = f[x] = f[f[x]];
        }
        return x;
    }

    bool same(int x, int y) {
        return find(x) == find(y);
    }

    bool merge(int x, int y) {
        x = find(x);
        y = find(y);
        if (x == y) {
            return false;
        }
        siz[x] += siz[y];
        f[y] = x;
        return true;
    }

    int size(int x) {
        return siz[find(x)];
    }
};
class Solution {
public:
    vector<int> processQueries(int c, vector<vector<int>>& connections, vector<vector<int>>& queries) {
        DSU dsu(c + 1);
        for (auto &con : connections) {
            dsu.merge(con[0], con[1]);
        }
        map<int, vector<int>> groups;
        for (int i = 1; i <= c; i++) {
            groups[dsu.find(i)].push_back(i);
        }
        for (auto &[_, group] : groups) {
            ranges::sort(group, greater<int>());
        }
        vector<int> valid(c + 1, 1);
        vector<int> ans;
        for (auto &q : queries) {
            if (q[0] == 1) {
                if (valid[q[1]]) {
                    ans.push_back(q[1]);
                } else {
                    int root = dsu.find(q[1]);
                    while (!groups[root].empty() && !valid[groups[root].back()]) {
                        groups[root].pop_back();
                    }
                    ans.push_back(groups[root].empty() ? -1 : groups[root].back());
                }
            } else {
                valid[q[1]] = 0;
            }
        }
        return ans;
    }
};