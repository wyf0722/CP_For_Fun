class Solution {
public:
    long long maxTotalValue(vector<int>& nums, int k) {
        int n = nums.size();
        SparseTable<int, decltype(get_max)> st_mx(nums, get_max);
        SparseTable<int, decltype(get_min)> st_mn(nums, get_min);
        auto f = [&](int l, int r) {
            return st_mx.query(l, r) - st_mn.query(l, r);
        };
        max_heap<tuple<int, int, int>> h;
        for (int i = 0; i < n; i++) {
            h.emplace(f(i, n - 1), i, n - 1);
        }

        i64 ans = 0;
        while (k--) {
            auto [val, l, r] = h.top();
            h.pop();
            ans += val;
            if (r > l) h.emplace(f(l, r - 1), l, r - 1);
        }
        return ans;
    }
};